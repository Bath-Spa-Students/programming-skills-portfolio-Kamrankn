# Define a variable with whitespace characters
name_with_whitespace = "\t \n Kamran \t \n"

print(name_with_whitespace)

print("Using lstrip():", name_with_whitespace.lstrip())

print("Using rstrip():", name_with_whitespace.rstrip())

# Use strip() to remove both leading and trailing whitespace
print("Using strip():", name_with_whitespace.strip())
